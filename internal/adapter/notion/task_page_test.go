func TestCreatePageSupportsMarkdownAndPosition(t *testing.T) {
	t.Setenv("NOTION_ACCESS_TOKEN", "notion-token")

	client := newTestClient(t, &roundTripFunc{
		handler: func(request *http.Request) (*http.Response, error) {
			if request.URL.Path != "/v1/pages" {
				t.Fatalf("unexpected request path: %s", request.URL.Path)
			}

			var payload map[string]any
			if err := json.NewDecoder(request.Body).Decode(&payload); err != nil {
				t.Fatalf("failed to decode create page request: %v", err)
			}
			if payload["markdown"] != "# 会议纪要\n\n今天完成了联调。" {
				t.Fatalf("unexpected markdown payload: %+v", payload)
			}
			position := payload["position"].(map[string]any)
			if position["type"] != "after_block" {
				t.Fatalf("unexpected position payload: %+v", position)
			}
			afterBlock := position["after_block"].(map[string]any)
			if afterBlock["id"] != "block_demo" {
				t.Fatalf("unexpected after_block payload: %+v", afterBlock)
			}
			properties := payload["properties"].(map[string]any)
			if _, ok := properties["title"]; !ok {
				t.Fatalf("expected title property in markdown create payload: %+v", payload)
			}

			return jsonResponse(t, http.StatusOK, map[string]any{
				"id":       "page_markdown_123",
				"url":      "https://www.notion.so/page_markdown_123",
				"in_trash": false,
				"parent": map[string]any{
					"type":    "page_id",
					"page_id": "page_demo",
				},
				"properties": map[string]any{
					"title": map[string]any{
						"title": []map[string]any{
							{
								"type":       "text",
								"plain_text": "会议纪要",
								"text": map[string]any{
									"content": "会议纪要",
								},
							},
						},
					},
				},
			}), nil
		},
	})

	data, appErr := client.CreatePage(context.Background(), testStaticProfile(), map[string]any{
		"parent": map[string]any{
			"type": "page_id",
			"id":   "page_demo",
		},
		"title":    "会议纪要",
		"markdown": "# 会议纪要\n\n今天完成了联调。",
		"after":    "block_demo",
	})
	if appErr != nil {
		t.Fatalf("CreatePage returned error: %+v", appErr)
	}
	if data["page_id"] != "page_markdown_123" {
		t.Fatalf("unexpected page_id: %+v", data["page_id"])
	}
}

