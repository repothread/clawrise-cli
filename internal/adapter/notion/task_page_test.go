func TestUpsertMarkdownChildPageUpdatesExistingPage(t *testing.T) {
	t.Setenv("NOTION_ACCESS_TOKEN", "notion-token")

	client := newTestClient(t, &roundTripFunc{
		handler: func(request *http.Request) (*http.Response, error) {
			switch request.URL.Path {
			case "/v1/search":
				var payload map[string]any
				if err := json.NewDecoder(request.Body).Decode(&payload); err != nil {
					t.Fatalf("failed to decode search request: %v", err)
				}
				if payload["query"] != "日报" {
					t.Fatalf("unexpected search payload: %+v", payload)
				}
				return jsonResponse(t, http.StatusOK, map[string]any{
					"results": []map[string]any{
						{
							"object": "page",
							"id":     "page_existing_123",
							"url":    "https://www.notion.so/page_existing_123",
							"parent": map[string]any{
								"type":    "page_id",
								"page_id": "page_parent_123",
							},
							"properties": map[string]any{
								"title": map[string]any{
									"title": []map[string]any{
										{
											"type":       "text",
											"plain_text": "日报",
											"text": map[string]any{
												"content": "日报",
											},
										},
									},
								},
							},
						},
					},
					"has_more": false,
				}), nil
			case "/v1/pages/page_existing_123/markdown":
				var payload map[string]any
				if err := json.NewDecoder(request.Body).Decode(&payload); err != nil {
					t.Fatalf("failed to decode markdown update request: %v", err)
				}
				if payload["type"] != "replace_content" {
					t.Fatalf("unexpected markdown update type: %+v", payload)
				}
				replaceContent := payload["replace_content"].(map[string]any)
				if replaceContent["new_str"] != "# 日报\n\n今天推进了集成测试。" {
					t.Fatalf("unexpected replace_content payload: %+v", replaceContent)
				}
				return jsonResponse(t, http.StatusOK, map[string]any{
					"object":            "page_markdown",
					"id":                "page_existing_123",
					"markdown":          "# 日报\n\n今天推进了集成测试。",
					"truncated":         false,
					"unknown_block_ids": []string{},
				}), nil
			default:
				t.Fatalf("unexpected request path: %s", request.URL.Path)
				return nil, nil
			}
		},
	})

	data, appErr := client.UpsertMarkdownChildPage(context.Background(), testStaticProfile(), map[string]any{
		"parent_page_id": "page_parent_123",
		"title":          "日报",
		"markdown":       "# 日报\n\n今天推进了集成测试。",
	})
	if appErr != nil {
		t.Fatalf("UpsertMarkdownChildPage returned error: %+v", appErr)
	}
	if data["action"] != "updated" || data["page_id"] != "page_existing_123" {
		t.Fatalf("unexpected upsert update result: %+v", data)
	}
}

