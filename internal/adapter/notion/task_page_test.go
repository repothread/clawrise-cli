func TestUpsertMarkdownChildPageRejectsAmbiguousMatches(t *testing.T) {
	t.Setenv("NOTION_ACCESS_TOKEN", "notion-token")

	client := newTestClient(t, &roundTripFunc{
		handler: func(request *http.Request) (*http.Response, error) {
			if request.URL.Path != "/v1/search" {
				t.Fatalf("unexpected request path: %s", request.URL.Path)
			}
			return jsonResponse(t, http.StatusOK, map[string]any{
				"results": []map[string]any{
					{
						"object": "page",
						"id":     "page_dup_1",
						"parent": map[string]any{
							"type":    "page_id",
							"page_id": "page_parent_123",
						},
						"properties": map[string]any{
							"title": map[string]any{
								"title": []map[string]any{
									{
										"type":       "text",
										"plain_text": "日报",
										"text": map[string]any{
											"content": "日报",
										},
									},
								},
							},
						},
					},
					{
						"object": "page",
						"id":     "page_dup_2",
						"parent": map[string]any{
							"type":    "page_id",
							"page_id": "page_parent_123",
						},
						"properties": map[string]any{
							"title": map[string]any{
								"title": []map[string]any{
									{
										"type":       "text",
										"plain_text": "日报",
										"text": map[string]any{
											"content": "日报",
										},
									},
								},
							},
						},
					},
				},
				"has_more": false,
			}), nil
		},
	})

	_, appErr := client.UpsertMarkdownChildPage(context.Background(), testStaticProfile(), map[string]any{
		"parent_page_id": "page_parent_123",
		"title":          "日报",
		"markdown":       "# 日报",
	})
	if appErr == nil {
		t.Fatal("expected UpsertMarkdownChildPage to reject ambiguous matches")
	}
	if appErr.Code != "AMBIGUOUS_TARGET" {
		t.Fatalf("unexpected error code: %s", appErr.Code)
	}
}
